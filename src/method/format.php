/* ------------------------------------------------------------------- SECOND --- */
function format_second($ss) {
    $second = ($ss % 60);
    $minute = floor(($ss % 3600) / 60);
    $hour = floor(($ss % 86400) / 3600);
    $day = floor(($ss % 2592000) / 86400);
    
    return (object) [
        'day' => $day,
        'hour' => $hour,
        'minute' => $minute,
        'second' => $second,
    ];
}
